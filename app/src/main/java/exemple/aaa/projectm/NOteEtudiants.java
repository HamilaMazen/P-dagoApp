public class NOteEtudiants {
    String nomEtu;
    String noteEtu;


    public NOteEtudiants(String nomEtu, String noteEtu) {
        this.nomEtu = nomEtu;
        this.noteEtu = noteEtu;
    }

    public String getNomEtu() {
        return nomEtu;
    }

    public String getNoteEtu() {
        return noteEtu;
    }
}
